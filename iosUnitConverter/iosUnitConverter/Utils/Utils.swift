    static func convertTime(time: Date, toPattern: String) -> String{
        let outFormatter = DateFormatter()
        outFormatter.locale = NSLocale(localeIdentifier: "en_US_POSIX") as Locale
        outFormatter.dateFormat = toPattern
        
        let outStr = outFormatter.string(from: time)
        return outStr.description
    }
    
    static func isHigherVersion(v1: String, v2: String) -> Bool {
        if v1.isEmpty && v2.isEmpty {
            return false
        } else if v1.isEmpty {
            return false
        } else if v2.isEmpty {
            return true
        }
        let v1Components = v1.split(separator: ".").map { Int($0) ?? 0 }
        let v2Components = v2.split(separator: ".").map { Int($0) ?? 0 }
        let minLength = min(v1Components.count, v2Components.count)
        print("isHigherVersion \(v1Components) vs \(v2Components)")
        for i in 0..<minLength {
            print("isHigherVersion at \(i): \(v1Components[i]) vs \(v2Components[i])")
            if v1Components[i] > v2Components[i] {
                return true
            } else if v1Components[i] < v2Components[i] {
                return false
            }
        }
        return v1Components.count > v2Components.count
    }

