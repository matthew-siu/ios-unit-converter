    public static func decode<T: Codable>(str: String?, to type: T.Type) -> T?{
        if let string = str{
            do {
                return try JSONDecoder().decode(T.self, from: Data(string.utf8))
            }
            catch { print("decode error: \(error.localizedDescription)\n\(string)")}
            return nil
        }else{
            return nil
        }
        
    }
    public static func decode<T: Codable>(obj: [String: Any]?, to type: T.Type) -> T?{
        if let obj = obj{
            do {
                let jsonData = try JSONSerialization.data(withJSONObject: obj, options: [])
                let object = try JSONDecoder().decode(T.self, from: jsonData)
                return object
            }
            catch { print("decode error: \(error.localizedDescription)")}
            return nil
        }else{
            return nil
        }
        
    }
    
